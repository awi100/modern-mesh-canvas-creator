// Function to find nearest DMC color using weighted RGB distance
export function findNearestDMC(rgb, palette = dmcColors) {
  let minDist = Infinity;
  let nearest = palette[0];
  
  for (const color of palette) {
    // Using weighted Euclidean distance (human eye is more sensitive to green)
    const rMean = (rgb[0] + color.rgb[0]) / 2;
    const r = rgb[0] - color.rgb[0];
    const g = rgb[1] - color.rgb[1];
    const b = rgb[2] - color.rgb[2];
    
    // Weighted distance formula for better perceptual matching
    const dist = Math.sqrt(
      (2 + rMean / 256) * r * r +
      4 * g * g +
      (2 + (255 - rMean) / 256) * b * b
    );
    
    if (dist < minDist) {
      minDist = dist;
      nearest = color;
    }
  }
  
  return nearest;
}

// Function to get a subset of colors for a given color count
export function getColorPalette(colors, count) {
  // If requesting all colors, return the full palette
  if (count >= dmcColors.length) return dmcColors;
  
  // For smaller counts, we'll use the colors that were actually 
  // selected through the quantization process
  return colors.slice(0, count);
}

